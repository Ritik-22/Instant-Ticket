    public void fillTrainjTable(JTable table ,String des){
        try
        {

            Class.forName("com.mysql.jdbc.Driver");

            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/railway_database", "root", "root");
            PreparedStatement ps;
            if(des=="")
            {
                 JOptionPane.showMessageDialog(rootPane, "Enter Destination");
            }
            else
            {
                ps=con.prepareStatement("Select * from train natural join schedule WHERE (destination LIKE ? or idtrain in (select idtrain from passes natural join station where stationName like ?)) and arrtime > CURTIME()");
                ps.setString(1,"%"+des+"%");
                ps.setString(2,"%"+des+"%");
            
              
               
                ResultSet rs =ps.executeQuery();
                DefaultTableModel model =(DefaultTableModel) table.getModel();
            
                Object[] row;
                int i=1;
                while(rs.next())
                {
                    row = new Object[6];
                    row[0]=i++;
                    row[1]=rs.getString(1);
                    
                    row[2]=rs.getString(2);
                    row[3]=rs.getString(3);
                    
                    row[4]=rs.getTime(7);
                    row[5]=rs.getTime(8);
                
                    model.addRow(row);
            }
            }
        }
        catch (SQLException ex)
        {
            System.out.print("exception is" + ex);
        }
        catch (ClassNotFoundException ex)
        {
            System.out.print("exception is" + ex);
        }
    
    } 
