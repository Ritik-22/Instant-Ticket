    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        float bf=5.0f;
        nA=Integer.parseInt(jComboBox1.getSelectedItem().toString());
        nC=Integer.parseInt(jComboBox2.getSelectedItem().toString());
        destination=dest.getText();
        try
        {

            Class.forName("com.mysql.jdbc.Driver");

            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/railway_database", "root", "root");
            PreparedStatement ps,ps2;
           
            
                ps=con.prepareStatement("Select baseFare from train where idtrain = ?");
                ps.setInt(1,Integer.parseInt(trainNo));
                ps2=con.prepareStatement("Select distance from passes where idtrain = ? and idstation=(select idstation from station where stationName like ? )");
                ps2.setInt(1,Integer.parseInt(trainNo));
                ps2.setString(2,destination);
                ResultSet rs =ps.executeQuery();
                ResultSet rs2 =ps2.executeQuery();
           
                if(rs.next()&&rs2.next())
                {
                    bf=rs.getFloat(1);
                    distance=rs2.getInt(1);
                }
        }
        catch (SQLException ex)
        {
            System.out.print("exception is" + ex);
        }
        catch (ClassNotFoundException ex)
        {
            System.out.print("exception is" + ex);
        }
        
        totalFare = (nA*distance*bf)/10 + ((nC*distance*bf)/10)/2;
        tfare.setText(String.valueOf(totalFare));
    }//GEN-LAST:event_jButton2ActionPerformed

